# SECCIÓN 3 – Operadores en Python

# Python como calculadora
print(7 + 5)

# Exponenciación
print(3 ** 2)
print(3 ** 2.)
print(3. ** 2)
print(3. ** 2.)

# Multiplicación
print(4 * 5)
print(4 * 5.)
print(4. * 5)
print(4. * 5.)

# División
print(12 / 4)
print(12 / 4.)
print(12. / 4)
print(12. / 4.)

# División entera
print(12 // 4)
print(12 // 4.)
print(12. // 4)
print(12. // 4.)

print(15 // 4)
print(15. // 4)

print(-15 // 4)
print(15. // -4)

# Módulo (residuo)
print(17 % 5)
print(18 % 5.5)

# Suma
print(-6 + 6)
print(-3. + 9)

# Resta y operadores unarios
print(-7 - 3)
print(9. - 4)
print(-2.5)
print(+8)

# Prioridad de operadores
print(4 + 2 * 6)

# Enlazado (izquierda)
print(11 % 7 % 3)

# Exponenciación (derecha)
print(3 ** 2 ** 2)

# Ejemplo importante
print(-4 ** 2)
print(-3 ** 2)
print(-(4 ** 2))

# Expresión con prioridad
print(4 * 5 % 6)

# Expresión con paréntesis
print((6 * ((18 % 7) + 50) / (3 * 5)) // 2)